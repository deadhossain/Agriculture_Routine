    public void loginCode(){
        HashMap hm = new HashMap();
        
        boolean check_input = checkInput();
        if(check_input == true)
        {
            hm.clear();
            hm.put("user_name", txtUserName.getText());
            hm.put("password", txtPassword.getText());
            HashMap<Integer,HashMap> login_validation = new HashMap<>();

            login_validation = dc.getAllInformation("farmer_tb"," where user_name = '"+ txtUserName.getText() + "' and password = '" + txtPassword.getText() +"'");
            int j = 0;
            if(!login_validation.isEmpty())
            {
                
                for (Object key : login_validation.keySet())
                {
                    this.farmer_id =  login_validation.get(key).get("id").toString();
                    this.memeber_type = login_validation.get(key).get("type").toString();
                    this.Username = login_validation.get(key).get("name").toString();
                    //model.addRow(new Object[]{plant_setup.get(key).get("id"),plant_setup.get(key).get("plant_name"),plant_setup.get(key).get("description")});
                }
                System.out.println(this.farmer_id);
                System.out.println(this.memeber_type);
                this.setVisible(false);
                
                if(this.memeber_type.equals("Admin")){
                    AdminPanel m = new AdminPanel(this.farmer_id,this.memeber_type,"Welcome "+this.Username);
                    m.setVisible(true);
                }
                else{
                    UserPanel m = new UserPanel(this.farmer_id,this.memeber_type,"Welcome "+this.Username);
                    m.setVisible(true);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Wrong Username or Password","Input Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Please Fill Up all requred fields","Input Error",JOptionPane.ERROR_MESSAGE);
        }
    }
