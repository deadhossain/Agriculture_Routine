    public boolean checkTextBoxInput(JTextField jt)
    {
        if(jt.getText().isEmpty())
        {
            jt.setBorder(empty);
            jt.requestFocus();
            return false;     
        }
        jt.setBorder(notEmpty);
        return true;
    }
    
